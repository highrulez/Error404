function base(
  partial: Omit<
    ChecklistTemplateTask,
    | "createdAt"
    | "updatedAt"
    | "createdBy"
    | "updatedBy"
    | "active"
    | "required"
    | "processType"
    | "reminderEnabled"
    | "firstReminderAfterWorkingDays"
    | "reminderFrequencyWorkingDays"
    | "maximumReminderCount"
    | "escalationAfterWorkingDays"
    | "escalationEmailRule"
    | "fixedEscalationEmail"
  > & {
    active?: boolean;
    required?: boolean;
    securityCritical?: boolean;
    executionMode?: ChecklistTemplateTask["executionMode"];
  }
): ChecklistTemplateTask {
  return {
    processType: "Offboarding",
    active: true,
    required: true,
    reminderEnabled: true,
    firstReminderAfterWorkingDays: 1,
    reminderFrequencyWorkingDays: 1,
    maximumReminderCount: 3,
    escalationAfterWorkingDays: 3,
    escalationEmailRule: "Admin",
    fixedEscalationEmail: "",
    securityCritical: false,
    executionMode: null,
    createdAt: SEED_AT,
    updatedAt: SEED_AT,
    createdBy: ACTOR,
    updatedBy: ACTOR,
    ...partial,
  };
}

